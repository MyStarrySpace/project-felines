/**
 * Build bibliography entries for GWAS gene function annotations.
 *
 * Reads gene-functions.json (from fetch-gene-functions.mjs) and generates:
 * 1. src/data/bibliography/gene-function-db.ts — Source entry with Citations
 *
 * Uses NCBI Gene RefSeq summaries as citation quotes (more concise than UniProt).
 * Falls back to UniProt function descriptions when NCBI summary is unavailable.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const geneData = JSON.parse(
  fs.readFileSync(path.resolve(__dirname, "gene-functions.json"), "utf-8")
);

// Read gwas-genes.tsx to get disease info for each gene
const gwasContent = fs.readFileSync(
  path.resolve(__dirname, "../src/data/landing/gwas-genes.tsx"),
  "utf-8"
);

// Parse disease for each gene
const diseaseMap = new Map();
const funcMap = new Map();
const blocks = gwasContent.split(/(?=\{\s*\n\s*id:)/);
for (const block of blocks) {
  const idMatch = block.match(/id:\s*"([^"]+)"/);
  const diseaseMatch = block.match(/disease:\s*"([^"]+)"/);
  const funcMatch = block.match(/function:\s*\n?\s*"([^"]+(?:\n\s*[^"]+)*)"/);
  if (idMatch) {
    diseaseMap.set(idMatch[1], diseaseMatch?.[1] || "AD");
    if (funcMatch) {
      funcMap.set(idMatch[1], funcMatch[1].replace(/\n\s*/g, " "));
    }
  }
}

// Genes to skip (already have specific citations)
const SKIP_GENES = new Set(["BIN1", "TREM2", "PICALM", "SPI1", "APOE"]);

// Build citations
const citations = [];
const skipped = [];
const noData = [];

for (const [geneId, entry] of Object.entries(geneData)) {
  if (SKIP_GENES.has(geneId)) {
    skipped.push(geneId);
    continue;
  }

  // Get the best function description (prefer NCBI summary, fallback to UniProt)
  let quote = null;
  let source = null;
  for (const sym of entry.symbols) {
    if (sym.summary) {
      quote = sym.summary;
      source = "ncbi";
      break;
    }
    if (sym.uniprotFunction) {
      quote = sym.uniprotFunction;
      source = "uniprot";
      break;
    }
  }

  if (!quote) {
    noData.push(geneId);
    continue;
  }

  // Clean up the quote
  // Remove "[provided by RefSeq, ...]" suffix
  quote = quote.replace(/\s*\[provided by RefSeq[^\]]*\]/g, "");
  // Trim trailing period if already present
  quote = quote.trim();

  // Get the primary gene symbol
  const primarySymbol = entry.symbols[0]?.symbol || geneId;

  // Create citation ID using gene ID (normalized)
  const citationSlug = geneId.toLowerCase().replace(/[^a-z0-9]/g, "-");

  citations.push({
    citationId: `ncbi-gene-${citationSlug}`,
    geneId,
    symbol: primarySymbol,
    quote,
    source,
    disease: diseaseMap.get(geneId) || "AD",
    functionField: funcMap.get(geneId) || "",
  });
}

console.log(`Citations generated: ${citations.length}`);
console.log(`Skipped (specific cites): ${skipped.length} — ${skipped.join(", ")}`);
console.log(`No data: ${noData.length} — ${noData.join(", ")}`);

// Generate the TypeScript file
const lines = [];
lines.push(`import type { Source } from "./types";`);
lines.push(``);
lines.push(`/**`);
lines.push(` * Auto-generated gene function annotations from NCBI Gene (RefSeq summaries).`);
lines.push(` * Generated by scripts/build-gene-bibliography.mjs`);
lines.push(` *`);
lines.push(` * Source: NCBI Gene database (RefSeq summaries) and UniProt function annotations.`);
lines.push(` * Each citation contains the database function description for one GWAS risk gene.`);
lines.push(` */`);
lines.push(``);
lines.push(`export const geneFunctionDbSources: Source[] = [`);
lines.push(`  {`);
lines.push(`    id: "ncbi-gene-refseq",`);
lines.push(`    title:`);
lines.push(`      "NCBI Gene: gene-centered information at NCBI",`);
lines.push(`    authors: "NCBI Resource Coordinators",`);
lines.push(`    journal: "Nucleic Acids Research",`);
lines.push(`    year: 2025,`);
lines.push(`    doi: "10.1093/nar/gkae1171",`);
lines.push(`    pmid: "39578685",`);
lines.push(`    url: "https://www.ncbi.nlm.nih.gov/gene/",`);
lines.push(`    tags: ["genetics"],`);
lines.push(`    citations: [`);

for (const cit of citations) {
  // Escape quote for TypeScript string
  const escapedQuote = cit.quote
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\n/g, " ");

  lines.push(`      {`);
  lines.push(`        citationId: "${cit.citationId}",`);
  lines.push(`        sourceId: "ncbi-gene-refseq",`);
  lines.push(`        quote:`);
  lines.push(`          "${escapedQuote}",`);
  lines.push(`        context: "${cit.symbol} gene function (${cit.source === "ncbi" ? "RefSeq summary" : "UniProt function"})",`);
  lines.push(`        projectRef: "GWAS genes: ${cit.geneId} functional description",`);
  lines.push(`      },`);
}

lines.push(`    ],`);
lines.push(`    verificationStatus: "verified",`);
lines.push(`  },`);
lines.push(`];`);
lines.push(``);

const outPath = path.resolve(
  __dirname,
  "../src/data/bibliography/gene-function-db.ts"
);
fs.writeFileSync(outPath, lines.join("\n"));
console.log(`\nWrote ${outPath}`);
console.log(`  ${citations.length} citations in 1 source entry`);

// Also save a mapping file for the transformation script
const mapping = {};
for (const cit of citations) {
  mapping[cit.geneId] = {
    citationId: cit.citationId,
    disease: cit.disease,
  };
}
const mapPath = path.resolve(__dirname, "gene-citation-map.json");
fs.writeFileSync(mapPath, JSON.stringify(mapping, null, 2));
console.log(`Wrote citation mapping to ${mapPath}`);
